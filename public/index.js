// Put bible in array---------------------------------
function start() {
  while (i < 200) {
    while (j < 500) {
      let firstIndex = chapter.indexOf(i + ":" + j + " ");
      let lastIndex = chapter.indexOf(i + ":" + (j + +"1") + " ")
      let verse = chapter.slice(firstIndex, lastIndex)
      if (verse.length > 500) {
        lastIndex = chapter.indexOf(i + 1 + ":1 ")
        verse = chapter.slice(firstIndex, lastIndex)
        chapterArray.push(verse)
        break;
      }
      chapterArray.push(verse)
      j++
      if (verse === "") {
        break
      };
    };
    i++;
    j = 1
  };
  showBible()
};

function raiseValue() {
  k += 1
  showBible()
}

// show verse on the page from array---------------------
function showBible() {
  if (k === 0) {
    document.querySelector('.bibleTyping').value = ""
    document.querySelector(".hideAndOpen").style.display = "block"
    document.querySelector(".book123").innerHTML = nameOfBook
    document.querySelector(".original1").innerHTML = chapterArray[k]
    document.querySelector(".original2").innerHTML = chapterArray[k + 1]
    document.querySelector(".original3").innerHTML = chapterArray[k + 2]
    document.querySelector(".original1").style.color = "black"

  } else {
    document.querySelector('.bibleTyping').value = ""
    document.querySelector(".hideAndOpen").style.display = "block"
    document.querySelector(".book123").innerHTML = nameOfBook
    document.querySelector(".original1").innerHTML = chapterArray[k - 1]
    document.querySelector(".original2").innerHTML = chapterArray[k]
    document.querySelector(".original3").innerHTML = chapterArray[k + 1]
    document.querySelector(".original2").style.color = "black"
  }
};



function valuePass() {

  let value = document.querySelector('.bibleTyping').value


  checkLetter(value)
}


function checkLetter(value) {
  let d = new Date();
  timeNow.push(d.getTime())
  let tpSpeed = parseInt(value.length / ((timeNow[timeNow.length - 1] - timeNow[0]) / 60000));
  if (k === 0) {
    if (value === chapterArray[k].slice(0, value.length)) {

      document.querySelector(".typeSpeed").innerHTML = tpSpeed
      document.querySelector(".original1").innerHTML = value
      document.querySelector(".original1").style.color = "#1cc5dc"
      document.querySelector(".restWord1").innerHTML = chapterArray[k].slice(value.length, chapterArray[k].length)
      document.querySelector(".restWord1").style.color = "black"
      document.querySelector(".hey123").innerHTML = "good"
      if (value.length + 2 === chapterArray[k].length) {
        document.querySelector(".avgSpeed").innerHTML = parseInt(avgTpSpeed())
        speedPerVerse.push(tpSpeed)
        timeNow = []
        document.querySelector('.bibleTyping').value = ""
        document.querySelector(".original1").style.color = "#926e6f"
        raiseValue()
      }
    } else {
      wrong()
    }
  } else {
    if (value === chapterArray[k].slice(0, value.length)) {
      document.querySelector(".typeSpeed").innerHTML = tpSpeed
      document.querySelector(".original2").innerHTML = value
      document.querySelector(".original2").style.color = "#1cc5dc"
      document.querySelector(".restWord2").innerHTML = chapterArray[k].slice(value.length, chapterArray[k].length)
      document.querySelector(".restWord2").style.color = "black"
      document.querySelector(".hey123").innerHTML = "good"
      if (value.length + 2 === chapterArray[k].length) {
        speedPerVerse.push(tpSpeed)
        document.querySelector(".avgSpeed").innerHTML = parseInt(avgTpSpeed())
        timeNow = []
        document.querySelector('.bibleTyping').value = ""
        document.querySelector(".original2").style.color = "#926e6f"
        raiseValue()
      }

    } else {
      wrong()
    }
  }
}

function avgTpSpeed() {
  let sum = 0
  speedPerVerse.forEach(e => sum = sum + e)
  return sum / speedPerVerse.length
}

